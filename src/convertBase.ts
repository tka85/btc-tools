export const bin2dec = (s) => parseInt(s, 2).toString(10);
export const bin2hex = (s) => parseInt(s, 2).toString(16);
export const dec2bin = (s) => parseInt(s, 10).toString(2);
export const dec2hex = (s) => parseInt(s, 10).toString(16);
export const hex2bin = (s) => parseInt(s, 16).toString(2);
export const hex2dec = (s) => parseInt(s, 16).toString(10);